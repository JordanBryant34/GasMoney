import UIKit.UIColor

extension UIColor {
    
    static func offWhite() -> UIColor {
        return UIColor(red: 230/255, green: 230/255, blue: 227/255, alpha: 1)
    }
    
    static func gasGreen() -> UIColor {
        return UIColor(red: 75/255, green: 166/255, blue: 61/255, alpha: 1)
    }
    
    static func subLabelGray() -> UIColor {
        return UIColor(red: 174/255, green: 177/255, blue: 173/255, alpha: 1)
    }
    
    static func gasGray() -> UIColor {
        return UIColor(red: 132/255, green: 132/255, blue: 140/255, alpha: 1)
    }
    
}
